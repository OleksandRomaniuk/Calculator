package src.util;

/**
 * {@code ProgramElement} is an enumeration of program elements
 * that can be used in {@link ProgramFactory}.
 */

public enum ProgramElement {
    NUMBER,
    NUMERIC_EXPRESSION,
    RELATIONAL_EXPRESSION,
    EXPRESSION,
    OPERAND,
    BRACKETS,
    FUNCTION,
    STATEMENT,
    INIT_VAR,
    PROGRAM,
    PROCEDURE,
    READ_VARIABLE,
    WHILE_OPERATOR,
    BOOLEAN_EXPRESSION,
    BOOLEAN_OPERAND
}
